package main

import "fmt"

func main() {
	// declaration boolean
	var isLogin bool = false
	fmt.Println(isLogin)
	
	// declaration numeric integer
	var prime int = 7
	fmt.Println(prime)
	
	// declaratio decimal number
	var decimal float64 = 45.6
	fmt.Println(decimal)
	
	// declaration string
	var hello string = "Hello World!"
	fmt.Println(hello)
	
	// declaration const
	const pi = 3.14159265358979323846
	fmt.Println(pi)
}

